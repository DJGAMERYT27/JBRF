let parser = new DOMParser();

window.onload = function() {
    var inputField = document.getElementById("inputSearch");
    inputField.addEventListener('input', function() {
        filtrarBusqueda(inputField.value);
    });

    document.getElementById("contenedor-tarjetas").innerHTML = tarjetas;
}

function filtrarBusqueda(entrada) {
  var copia = parser.parseFromString(tarjetas, 'text/html').getElementsByClassName("col");
  var found = Array.from(copia).filter(
      tarjeta => tarjeta.innerHTML.includes(entrada)
  );
  
  var contenedorTarjetas = document.getElementById("contenedor-tarjetas");
  contenedorTarjetas.innerHTML = "";
  found.forEach(element => {
      contenedorTarjetas.appendChild(element);
  });
}


